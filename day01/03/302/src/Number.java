public class Number {
    //主方法
    public static void main(String[] args) {
        //声明byte类型变量并赋值
        byte mybyte = 124;
        //声明short型变量并赋值
        short myshort = 32564;
        //声明int型变量并赋值
        int myint = 45784612;
        //声明long型变量并赋值
        int mylong = 46789451;
        long result = mybyte + myshort + myint + mylong;
        System.out.println("结果为："+result);
    }
}
